package org.dia.part01.lesson03.code;

public class MiscellaneousOperators {
    public static void main(String[] args) {
        int a = 6;

        /*

        a > 5  -> "Hello!"
        a <= 5 -> "No permission!"

         */

        // ternary operator
        System.out.println((a > 5) ? "Hello!" : "No permission!");
    }
}
